/**
 * レベルからステージ情報を取得する
 * レベルが存在しない場合は、最も近いレベルの情報を返す
 */
export function getMoteStage(level: number): MoteStage {
  // レベルを-100から100の範囲にクランプ
  const clampedLevel = Math.max(-100, Math.min(100, Math.round(level)));
  
  // 直接マッチする場合はそれを返す
  if (MOTE_STAGES[clampedLevel]) {
    return MOTE_STAGES[clampedLevel];
  }
  
  // マッチしない場合は、最も近いレベルを探す
  // まずはプラス方向、次にマイナス方向
  for (let offset = 1; offset <= 200; offset++) {
    if (MOTE_STAGES[clampedLevel + offset]) {
      return MOTE_STAGES[clampedLevel + offset];
    }
    if (MOTE_STAGES[clampedLevel - offset]) {
      return MOTE_STAGES[clampedLevel - offset];
    }
  }
  
  // フォールバック（通常は到達しない）
  return { name: "不明", desc: "ステージ情報が見つかりません" };
}

